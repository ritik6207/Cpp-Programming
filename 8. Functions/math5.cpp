// Trigonometic sine, cosine  and tangent of x (x in radians)
#include <iostream>
#include <cmath>
using namespace std;
int main()
{
    double x = 45, s, c, t;
    s = sin(x);
    c = cos(x);
    t = tan(x);
    cout << s << endl;
    cout << c << endl;
    cout << t << endl;
    return 0;
}